package com.syntax.class3;

public class DoubleComparision {

	public static void main(String[] args) {
		// compare two double values
		double d1 = 10.99;
		double d2 = 2.99;
		
		if (d1!=d2)
			System.out.println("Double value d1="+d1+" is larger than double value d2="+d2);
		else
			System.out.println("Double value d1="+d1+" is smaller than double value d2="+d2);

	}

}
